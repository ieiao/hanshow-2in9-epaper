volatile int ta_icnt = 0;

void delay_ms(int i)
{
    ta_icnt = 0;
    TACTL = ID_3 + TASSEL_2 + MC_1;
    while (ta_icnt < i);
    TACTL = ID_3 + TASSEL_2 + MC_0;
}

void pin_init()
{
    P2SEL &= ~(BIT3 | BIT4 | BIT5 | BIT6);
    P2SEL2 &= ~(BIT3 | BIT4 | BIT5 | BIT6);

    P3SEL &= ~(BIT1 | BIT4 | BIT5 | BIT6 | BIT7);
    P3SEL2 &= ~(BIT1 | BIT4 | BIT5 | BIT6 | BIT7);

    P2DIR &= ~BIT5;
    P2DIR |= (BIT3 | BIT4);
    P3DIR |= (BIT1 | BIT4 | BIT5 | BIT6 | BIT7);
}

void epd_bus_mod_select(void)
{
    /* 4 line spi */
    P3OUT &= ~BIT1;
    P3OUT |= BIT7;
    P2OUT |= BIT6;
    delay_ms(10);
}

void epd_wait_until_idle(void)
{
    while((P2IN & BIT5) == 1)
        delay_ms(10);
}

void epd_reset(void)
{
    P3OUT |= BIT6;
    delay_ms(200);
    P3OUT &= ~BIT6;
    delay_ms(10);
    P3OUT |= BIT6;
    delay_ms(200);
}

void epd_send_command(char command)
{
    volatile char i;
    P3OUT &= ~BIT5;
    P2OUT &= ~(BIT3 | BIT4);
    P3OUT &= ~BIT4;

    for (i = 0; i < 8; i++) {
        if (command & 0x80)
            P2OUT |= BIT4;
        else
            P2OUT &= ~BIT4;
        P2OUT |= BIT3;
        P2OUT &= ~BIT3;
        command = command << 1;
    }
    P3OUT |= BIT4;
}

void epd_send_data(char dat)
{
    volatile char i;
    P3OUT |= BIT5;
    P2OUT &= ~(BIT3 | BIT4);
    P3OUT &= ~BIT4;

    for (i = 0; i < 8; i++) {
        if (dat & 0x80)
            P2OUT |= BIT4;
        else
            P2OUT &= ~BIT4;
        P2OUT |= BIT3;
        P2OUT &= ~BIT3;
        dat = dat << 1;
    }
    P3OUT |= BIT4;
}

void ta_init(void)
{
    /* smclk 8M, ta input 8 divide */
    CCR0 = 1000;
    TACCTL0 = CCIE;
    TACTL = ID_3 + TASSEL_2 + MC_0;
}

int main(void)
{
    char i, j, k;
    const char *p=ni;
    WDTCTL = WDTPW + WDTHOLD; // Stop WDT
    if (CALBC1_8MHZ == 0xFF)  // If calibration constant erased
    {
        while (1)
            ; // do not load, trap CPU!!
    }
    DCOCTL = 0;            // Select lowest DCOx and MODx settings
    BCSCTL1 = CALBC1_8MHZ; // Set DCO
    DCOCTL = CALDCO_8MHZ;

    __bis_SR_register(GIE); // interrupts enabled
    ta_init();

    pin_init();
    epd_bus_mod_select();
    epd_iface_setup(epd_wait_until_idle, epd_reset,
                    epd_send_command, epd_send_data);
    epd_init();

    epd_draw(gImage_2in9);

    for (i = 0; i < 7; i++) {
        for(j = 0; j < 16; j++) {
            epd_set_cursor(0, j+16*i);
            epd_send_command(0x24);
            for (k = 0; k < 2; k++) {
                epd_send_data(*p++);
            }
        }
    }

    epd_turnon();

    __bis_SR_register(LPM3_bits);
    while(1);
}

